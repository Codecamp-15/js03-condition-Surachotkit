let input = prompt("number")

if(input > 0){
    console.log("Positive number");
    alert("Positive number")
}
else if(input == 0){
    console.log("Zero");
    alert("Zero")
}
else if(input < 0){
    console.log("Negative number");
    alert("Negative number")
}
else{
    console.log("Invalid number");
    alert("Invalid number")
}
